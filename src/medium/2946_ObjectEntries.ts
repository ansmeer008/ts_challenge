//undefined인 경우에도 값으로 들어가도록 하기 위해 따로 지정해주는 과정 필요
type ObjectEntries<T> = {
  [P in keyof T]-?: [P, T[P] extends infer R | undefined ? R : T[P]];
}[keyof T];

